    private void btCleanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCleanActionPerformed
        File folder = new File(tfFolderRoot.getText());
        File[] listOfFiles = folder.listFiles();
        if (!"".equals(tfFolderRoot.getText())) {
            for (File file : listOfFiles) {
                if (file.isFile()) {
                    if (getFileExtension(file).equals(tfFileTall.getText())) {
                        try {
                            File fileImage = new File(tfFolderRoot.getText() + "/" + tfFolderTarget.getText());
                            if (!fileImage.exists()) {
                                fileImage.mkdir();
                            }
                            Files.move(Paths.get(file.getPath()), Paths.get(fileImage.getPath() + "/" + file.getName()));
                        } catch (IOException ex) {
                            Logger.getLogger(cleanView.class.getName()).log(Level.SEVERE, null, ex);
                        }

                    }
                }
            }

        }

    }//GEN-LAST:event_btCleanActionPerformed

